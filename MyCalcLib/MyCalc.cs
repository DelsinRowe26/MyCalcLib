using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MyCalcLib
{
    public class MyCalc
    {
        public int Sum(int x, int y)
        {
            return x + y;
        }
        public int Vich(int x, int y)
        {
            return x - y;
        }
        public int Umn(int x, int y)
        {
            return x * y;
        }
        public int Del(int x, int y)
        {
            return x / y;
        }
    }
}
